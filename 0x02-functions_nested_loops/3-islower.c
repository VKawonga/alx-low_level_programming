/** 
 * _islower - prints whether or not a character is lower case or not
 *
 * Description -_islower prints whether or not a character is lower case or not
 * using islower function
 *
 * Returns: 1
 */
int _islower(int c)
{
	islower(c);
	if ((islower (c)) > '0');
	{
		return (1);
	}

	else;
	{
		return (0);
	}
}
